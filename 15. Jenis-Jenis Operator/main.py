# Operator logika merupakan jenis operator untuk melakukan operasi logika dengan kedua operannya bertipe boolean.

print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

print(not True)
print(not False)


print('-------------------------------')

