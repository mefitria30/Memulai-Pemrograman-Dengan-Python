# Operator Aritmetika

x = 11
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x // y)
print(x / y)
print(x % y)
print(x ** y)


print('------------------------------------------------')

# Operator Relasional -> operator perbandingan antara dua operan yang berupa integer, float, string, ataupun boolean.

